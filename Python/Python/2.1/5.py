math1 = int(input("What is 10 + 3 "))
math2 = int(input("What is 2 to the power of 3 "))
math3 = int(input("What is 5 - 8 "))

if math1 == 10 + 3:
    print("Your first answer was correct")
else:
    print(f"{10+3} is the correct answer")

if math2 == 2**3:
    print("Your second answer was correct")
else:
    print(f"{2**3} is the correct answer")

if math3 == 5-8:
    print("Your third answer was correct")
else:
    print(f"{5-8} is the correct answer")
