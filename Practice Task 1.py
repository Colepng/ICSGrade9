length = float(input("How long do you want the yard to be ")) #Gets the length of the yard
width = float(input("How wide do you want the yard to be ")) #Gets the width of the yard

area = length*width #Makes a variable called area and stores length times width
perimeter = 2*length + 2*width #Makes a variable called perimeter and stores 2length * 2width

print("The perimeter of the yard is", perimeter) #Outputs the perimeter of the yard
print("The area of the yard is", area) #Outputs the area of the yard


